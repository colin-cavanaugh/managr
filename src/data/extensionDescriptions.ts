/**
 * Returns a human-readable description for a file extension, or null if unknown.
 * @param ext — extension with dot, e.g. ".ba2" or "(none)"
 */
export function getExtDescription(ext: string): string | null {
  if (!ext || ext === '(none)') return null
  return descriptions[ext.toLowerCase()] ?? null
}
